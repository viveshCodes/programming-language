"""
In this section, we will explore the following values in Python:
1. Zero (0)
2. Boolean False
3. Empty String ("")
4. None
5. Empty Array (List)

We will also see how the `not` operator treats each of these values.
"""

# 1. Zero (0)
zero_value = 0
print(f'not of Zero (0): {not zero_value}')  # Output: True

# 2. Boolean False
false_value = False
print(f'not of Boolean False: {not false_value}')  # Output: True

# 3. Empty String ("")
empty_string = ""
print(f'not of Empty String (""): {not empty_string}')  # Output: True

# 4. None
none_value = None
print(f'not of None: {not none_value}')  # Output: True

# 5. Empty Array (List)
empty_array = []
print(f'not of Empty Array (List): {not empty_array}')  # Output: True
